/**
 * 확장 설정 초기화
 */
function initializeSettings() {
    if (!extension_settings[MODULE_NAME]) {
        extension_settings[MODULE_NAME] = {};
    }

    if (!extension_settings[MODULE_NAME].characterAssets) {
        extension_settings[MODULE_NAME].characterAssets = {};
    }

    if (!extension_settings[MODULE_NAME].imagePrompt) {
        extension_settings[MODULE_NAME].imagePrompt = DEFAULT_IMAGE_PROMPT;
    }

    // 프리셋 데이터 초기화
    if (!extension_settings[MODULE_NAME].presets) {
        extension_settings[MODULE_NAME].presets = DEFAULT_PRESETS.map(preset => ({
            id: uuidv4(),
            ...preset
        }));
        extension_settings[MODULE_NAME].currentPresetId = extension_settings[MODULE_NAME].presets[0].id;
    } else {
        const presetNames = new Set(extension_settings[MODULE_NAME].presets.map(preset => preset.name));
        DEFAULT_PRESETS.forEach(defaultPreset => {
            if (!presetNames.has(defaultPreset.name)) {
                extension_settings[MODULE_NAME].presets.push({
                    id: uuidv4(),
                    ...defaultPreset
                });
            }
        });
    }

    if (!extension_settings[MODULE_NAME].currentPresetId || !extension_settings[MODULE_NAME].presets.some(preset => preset.id === extension_settings[MODULE_NAME].currentPresetId)) {
        extension_settings[MODULE_NAME].currentPresetId = extension_settings[MODULE_NAME].presets[0].id;
    }

    if (typeof extension_settings[MODULE_NAME].enabled === 'undefined') {
        extension_settings[MODULE_NAME].enabled = true;
    }

    if (!extension_settings[MODULE_NAME].renderer) {
        extension_settings[MODULE_NAME].renderer = {};
    }

    if (typeof extension_settings[MODULE_NAME].renderer.enabled === 'undefined') {
        extension_settings[MODULE_NAME].renderer.enabled = false;
    }

    if (typeof extension_settings[MODULE_NAME].renderer.lazy === 'undefined') {
        extension_settings[MODULE_NAME].renderer.lazy = true;
    }

    if (typeof extension_settings[MODULE_NAME].renderer.randomAsset === 'undefined') {
        extension_settings[MODULE_NAME].renderer.randomAsset = false;
    }
}

