async function refreshRendererAssetCache() {
    rendererAssetCache = new Map();
    rendererRandomGroups = new Map();
    rendererProcessedMessages = new WeakSet();

    const isRandomMode = Boolean(getRendererSettings().randomAsset);

    function addAssetsToCache(assets, disabledSet) {
        assets.forEach(asset => {
            const fullFileNameWithQuery = asset.path.split('/').pop();
            const fileName = fullFileNameWithQuery.split('?')[0];
            if (disabledSet && disabledSet.has(fileName)) return;
            const baseName = fileName.split('.').slice(0, -1).join('.');
            rendererAssetCache.set(normalizeAssetKey(fileName), asset.path);
            rendererAssetCache.set(normalizeAssetKey(baseName), asset.path);
            rendererAssetCache.set(normalizeAssetKey(asset.label), asset.path);
            if (isRandomMode) {
                const groupKey = normalizeAssetKey(extractBaseKeyword(fileName));
                if (!rendererRandomGroups.has(groupKey)) rendererRandomGroups.set(groupKey, []);
                rendererRandomGroups.get(groupKey).push(asset.path);
            }
        });
    }

    if (isGroupMode()) {
        const memberIds = await getGroupCharacterIds();
        for (const memberId of memberIds) {
            initializeCharacterAssets(memberId);
            const charData = extension_settings[MODULE_NAME].characterAssets[memberId];
            const disabledSet = new Set(charData.disabledAssets || []);
            const assets = await fetchCharacterAssets(memberId);
            addAssetsToCache(assets, disabledSet);
        }
    } else {
        const character = getCurrentCharacter();
        if (!character) {
            return rendererAssetCache;
        }
        const characterName = character.avatar.replace(/\.[^/.]+$/, '');
        initializeCharacterAssets(characterName);
        const charData = extension_settings[MODULE_NAME].characterAssets[characterName];
        const disabledSet = new Set(charData.disabledAssets || []);
        const assets = await fetchCharacterAssets(characterName);
        addAssetsToCache(assets, disabledSet);
    }

    // 페르소나 에셋 캐시 빌드 (폴백용)
    rendererPersonaCache = new Map();
    try {
        const personaAssets = await fetchPersonaAssets();
        personaAssets.forEach(asset => {
            const fullFileNameWithQuery = asset.path.split('/').pop();
            const fileName = fullFileNameWithQuery.split('?')[0];
            const baseName = fileName.split('.').slice(0, -1).join('.');
            rendererPersonaCache.set(normalizeAssetKey(fileName), asset.path);
            rendererPersonaCache.set(normalizeAssetKey(baseName), asset.path);
            rendererPersonaCache.set(normalizeAssetKey(asset.label), asset.path);
        });
    } catch (e) {
        console.debug(`[${MODULE_NAME}] Persona asset cache build skipped:`, e);
    }

    return rendererAssetCache;
}

function resolveAssetUrl(assetName) {
    const trimmedName = String(assetName || '').trim();
    const key = normalizeAssetKey(trimmedName);

    if (Boolean(getRendererSettings().randomAsset) && rendererRandomGroups.size > 0) {
        const group = rendererRandomGroups.get(key);
        if (group && group.length > 0) {
            return group[Math.floor(Math.random() * group.length)];
        }
    }

    return rendererAssetCache.get(key) || rendererPersonaCache.get(key);
}

function renderMessageImages(messageElement) {
    if (!isBuiltinRendererEnabled() || !messageElement || rendererProcessedMessages.has(messageElement)) {
        return;
    }

    const textElement = messageElement.querySelector('.mes_text');
    if (!textElement || !textElement.innerHTML.includes('{{img::')) {
        return;
    }

    const lazyAttr = getRendererSettings().lazy ? ' loading="lazy"' : '';
    let modified = false;
    const nextHtml = textElement.innerHTML.replace(IMG_TAG_REGEX, (match, assetName) => {
        const assetUrl = resolveAssetUrl(assetName);
        if (!assetUrl) {
            return match;
        }

        modified = true;
        const safeName = escapeHtml(String(assetName).trim());
        const safeUrl = escapeHtml(assetUrl);
        return `<img class="character-asset-rendered" src="${safeUrl}" alt="${safeName}"${lazyAttr} onerror="this.style.display='none'">`;
    });

    if (modified) {
        textElement.innerHTML = nextHtml;
    }

    rendererProcessedMessages.add(messageElement);
}

let rendererVisibilityObserver = null;

function renderAllVisibleMessages() {
    if (!isBuiltinRendererEnabled()) return;
    document.querySelectorAll('#chat .mes').forEach(el => {
        if (rendererVisibilityObserver) {
            rendererVisibilityObserver.observe(el);
        } else {
            renderMessageImages(el);
        }
    });
}

function scheduleRendererPass() {
    if (!isBuiltinRendererEnabled()) return;

    if (typeof cancelIdleCallback === 'function' && rendererRenderTimer) {
        cancelIdleCallback(rendererRenderTimer);
    } else if (rendererRenderTimer) {
        clearTimeout(rendererRenderTimer);
    }
    rendererRenderTimer = null;

    const doWork = () => {
        rendererRenderTimer = null;
        renderAllVisibleMessages();
    };

    if (typeof requestIdleCallback === 'function') {
        rendererRenderTimer = requestIdleCallback(doWork, { timeout: 200 });
    } else {
        rendererRenderTimer = setTimeout(doWork, 80);
    }
}

async function resetRendererState() {
    rendererProcessedMessages = new WeakSet();
    await refreshRendererAssetCache();
    if (rendererVisibilityObserver) {
        rendererVisibilityObserver.disconnect();
    }
    scheduleRendererPass();
}

function setupBuiltinRenderer() {
    if (rendererObserver) {
        rendererObserver.disconnect();
        rendererObserver = null;
    }
    if (rendererVisibilityObserver) {
        rendererVisibilityObserver.disconnect();
        rendererVisibilityObserver = null;
    }

    if (!isBuiltinRendererEnabled()) return;

    const chatElement = document.getElementById('chat');
    if (!chatElement) return;

    rendererVisibilityObserver = new IntersectionObserver((entries) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                renderMessageImages(entry.target);
            }
        });
    }, { root: null, rootMargin: '200px 0px' });

    rendererObserver = new MutationObserver((mutations) => {
        for (const mutation of mutations) {
            for (const node of mutation.addedNodes) {
                if (node.nodeType === 1) {
                    if (node.classList?.contains('mes')) {
                        rendererVisibilityObserver.observe(node);
                    }
                    node.querySelectorAll?.('.mes')?.forEach(el => rendererVisibilityObserver.observe(el));
                }
            }
        }
        scheduleRendererPass();
    });

    rendererObserver.observe(chatElement, {
        childList: true,
        subtree: true,
        characterData: true
    });

    resetRendererState();
}

/**
 * ZIP 파일 업로드 처리
 * @param {File} file ZIP 파일
 */
async function handleZipUpload(file) {
    const character = getCurrentCharacter();
    if (!character) {
        showToast('error', '선택된 캐릭터가 없습니다.');
        return;
    }

    const characterId = character.avatar.replace(/\.[^/.]+$/, '');
    const formData = new FormData();
    formData.append('name', getEffectiveCharacterName(characterId));
    formData.append('avatar', file);

    const uploadToast = showToast('info', '업로드 중...', '처리 중입니다', { timeOut: 0, extendedTimeOut: 0 });

    try {
        // jQuery.ajax 사용 및 headers 제거
        const result = await jQuery.ajax({
            type: 'POST',
            url: '/api/sprites/upload-zip',
            data: formData,
            processData: false,
            contentType: false,
            cache: false
        });

        if (uploadToast) uploadToast.clear();
        showToast('success', `${result.count}개의 이미지가 업로드되었습니다.`, '업로드 성공');

        // 새로 업로드된 에셋 로드 및 자동 선택
        const assets = await fetchCharacterAssets(character.avatar.replace(/\.[^/.]+$/, ''));

        // 기존 이미지 키워드 목록 가져오기
        const characterAssets = getCharacterAssets(String(this_chid));
        const existingKeywords = characterAssets.imageKeywords || [];

        // 새 키워드 추출
        const newKeywords = new Set();
        assets.forEach(asset => {
            newKeywords.add(asset.label);
        });

        // 기존 키워드에 새 키워드 추가 (중복 방지)
        const updatedKeywords = [...existingKeywords];

        for (const keyword of newKeywords) {
            if (!existingKeywords.includes(keyword)) {
                updatedKeywords.push(keyword);
            }
        }

        // 새 키워드 목록 저장
        characterAssets.imageKeywords = updatedKeywords;
        saveSettingsDebounced();

        // UI 새로고침
        await loadCharacterAssets();
        await refreshImageKeywordsAuto(); // 키워드 자동 갱신

    } catch (error) {
        if (uploadToast) uploadToast.clear();
        console.error(`[${MODULE_NAME}] Error uploading ZIP:`, error);
        const errorText = error.responseText || '알 수 없는 오류';
        showToast('error', `업로드 중 오류가 발생했습니다. ${errorText}`, '업로드 실패');
    }
}

