/**
 * 캐릭터의 에셋 정보 초기화
 * @param {string} characterId 캐릭터 ID
 */
function initializeCharacterAssets(characterId) {
    if (!extension_settings[MODULE_NAME].characterAssets[characterId]) {
        extension_settings[MODULE_NAME].characterAssets[characterId] = {
            enabled: true,
            imageKeywords: [],
            disabledAssets: [],
            linkedCharacter: '',
            activeAssetPreset: '',
            assetPresets: [],
            regexFind: DEFAULT_REGEX_FIND,
            regexReplace: DEFAULT_REGEX_REPLACE,
            regexOptions: {
                userInput: false,
                aiOutput: true,
                runOnEdit: true,
                onlyFormatDisplay: true
            }
        };
    } else if (!extension_settings[MODULE_NAME].characterAssets[characterId].regexOptions) {
        extension_settings[MODULE_NAME].characterAssets[characterId].regexOptions = {
            userInput: false,
            aiOutput: true,
            runOnEdit: true,
            onlyFormatDisplay: true
        };
    }

    const charData = extension_settings[MODULE_NAME].characterAssets[characterId];
    if (!Array.isArray(charData.disabledAssets)) {
        charData.disabledAssets = [];
    }
    if (typeof charData.linkedCharacter !== 'string') {
        charData.linkedCharacter = '';
    }
    if (typeof charData.activeAssetPreset !== 'string') {
        charData.activeAssetPreset = '';
    }
    if (!Array.isArray(charData.assetPresets)) {
        charData.assetPresets = [];
    }
}

