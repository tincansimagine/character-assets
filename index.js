/**
 * 이미지 키워드를 자동으로 갱신하여 img_keywords_autogen 변수에 저장
 * @returns {Promise<string>} 갱신된 키워드 문자열
 */
function extractBaseKeyword(fileName) {
    const lastDot = fileName.lastIndexOf('.');
    const nameWithoutExt = lastDot > 0 ? fileName.substring(0, lastDot) : fileName;
    const match = nameWithoutExt.match(/^(.+?)(?:[_.\-]\d+)$/);
    return match ? match[1] : nameWithoutExt;
}

async function refreshImageKeywordsAuto() {
    try {
        let allAssets = [];

        if (isGroupMode()) {
            const memberIds = await getGroupCharacterIds();
            for (const memberId of memberIds) {
                initializeCharacterAssets(memberId);
                const charData = extension_settings[MODULE_NAME].characterAssets[memberId];
                const disabledSet = new Set(charData.disabledAssets || []);
                const assets = await fetchCharacterAssets(memberId);
                const active = assets.filter(a => {
                    const fn = a.path.split('/').pop().split('?')[0];
                    return !disabledSet.has(fn);
                });
                allAssets = allAssets.concat(active);
            }
        } else {
            const character = getCurrentCharacter();
            if (!character) {
                autoGeneratedKeywords = '';
                autoGeneratedKeywordsGrouped = '';
                return '';
            }
            const characterId = character.avatar.replace(/\.[^/.]+$/, '');
            initializeCharacterAssets(characterId);
            const charData = extension_settings[MODULE_NAME].characterAssets[characterId];
            const disabledSet = new Set(charData.disabledAssets || []);
            const assets = await fetchCharacterAssets(characterId);
            allAssets = assets.filter(a => {
                const fn = a.path.split('/').pop().split('?')[0];
                return !disabledSet.has(fn);
            });
        }

        const activeAssets = allAssets;
        const isRandomMode = Boolean(getRendererSettings().randomAsset);

        if (isRandomMode) {
            const baseKeywords = new Set();
            activeAssets.forEach(asset => {
                const fullFileNameWithQuery = asset.path.split('/').pop();
                const fileName = fullFileNameWithQuery.split('?')[0];
                baseKeywords.add(extractBaseKeyword(fileName));
            });
            const sorted = Array.from(baseKeywords).sort();
            autoGeneratedKeywords = sorted.join(', ');
        } else {
            const fileNames = new Set();
            activeAssets.forEach(asset => {
                const fullFileNameWithQuery = asset.path.split('/').pop();
                const fileName = fullFileNameWithQuery.split('?')[0];
                fileNames.add(fileName);
            });
            const sorted = Array.from(fileNames).sort();
            autoGeneratedKeywords = sorted.join(', ');
        }

        // 그룹핑된 키워드 생성 (e.g., "angry_[1, 2, 3], happy, sad")
        const groupMap = {};
        activeAssets.forEach(asset => {
            const fullFileNameWithQuery = asset.path.split('/').pop();
            const fileName = fullFileNameWithQuery.split('?')[0];
            const base = extractBaseKeyword(fileName);
            const nameWithoutExt = fileName.lastIndexOf('.') > 0 ? fileName.substring(0, fileName.lastIndexOf('.')) : fileName;
            if (!groupMap[base]) groupMap[base] = [];
            if (base !== nameWithoutExt) {
                const suffix = nameWithoutExt.substring(base.length).replace(/^[_.\-]/, '');
                if (suffix && !groupMap[base].includes(suffix)) groupMap[base].push(suffix);
            }
        });

        const groupedParts = Object.keys(groupMap).sort().map(base => {
            const suffixes = groupMap[base];
            if (suffixes.length === 0) return base;
            suffixes.sort((a, b) => {
                const na = parseInt(a), nb = parseInt(b);
                if (!isNaN(na) && !isNaN(nb)) return na - nb;
                return a.localeCompare(b);
            });
            return `${base}_[${suffixes.join(', ')}]`;
        });
        autoGeneratedKeywordsGrouped = groupedParts.join(', ');

        console.debug(`[${MODULE_NAME}] 이미지 키워드 자동 갱신 완료 (random=${isRandomMode}):`, autoGeneratedKeywords);
        console.debug(`[${MODULE_NAME}] 그룹핑된 키워드:`, autoGeneratedKeywordsGrouped);
        return autoGeneratedKeywords;
    } catch (error) {
        console.error(`[${MODULE_NAME}] 이미지 키워드 자동 갱신 중 오류:`, error);
        autoGeneratedKeywords = '';
        return '';
    }
}

